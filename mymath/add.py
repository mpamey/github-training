'''
Mathematical functions 
'''
def add(x, y):
    """Add two number together."""
    return x + y


def substract(x, y):
    """Substract two numbers together."""
    return x - y


def divide(x, y):
    """Divide two numbers together."""
    return x / y


def multiply(x, y):
    """Multiply two numbers together."""
    return x * y
